public class Demo {
    public static void main(String[] args){
        //Invert Half Pyramid
        System.out.println("Invert Half Pyramid");
        for(int i=5; i>5; i--){
            for(int j=1; j<=i; j++){
                System.out.print("*");
            }
            System.out.println("");
        }
    }
    
}
